#include "utils.h"

void	ft_putstr_fd(char *s, int fd)
{
	int x;

	if (!s)
		return ;
	x = 0;
	while (s[x] != '\0')
	{
		if (write(fd, &s[x], 1))
			x++;
		else
			return ;
	}
}
